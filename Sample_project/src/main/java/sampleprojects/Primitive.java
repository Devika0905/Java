package sampleprojects;

public class Primitive {
	public static void main(String []args) {
		int a =10;
		System.out.println("its an integer=" +a);
		char b ='a';
		System.out.println("its a character=" +b);
		String c ="Hello";
		System.out.println("its a string=" +c);
		float d =2.35f;
		System.out.println("its a float="+d);
		double e=2.345;
		System.out.println("its a double="+e);
		short f=1234;
		System.out.println("its a short="+f);
		long g=12345678;
		System.out.print("its a long="+g);
	}

}
